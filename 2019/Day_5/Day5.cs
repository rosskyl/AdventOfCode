        private static string RunProgram(int[] instructions, int input)
        {
            string output = "";

            int[] memory = new int[instructions.Length];
            instructions.CopyTo(memory, 0);

            int index = 0;

            while (memory[index] != 99)
            {
                var instruction = memory[index] % 100;

                if (instruction == 1 || instruction == 2 || instruction == 7 || instruction == 8)
                {
                    var mode1 = (memory[index] / 100) % 10;
                    var mode2 = (memory[index] / 1000) % 10;

                    int param1;
                    int param2;

                    if (mode1 == 0)
                        param1 = memory[memory[index + 1]];
                    else
                        param1 = memory[index + 1];

                    if (mode2 == 0)
                        param2 = memory[memory[index + 2]];
                    else
                        param2 = memory[index + 2];

                    if (instruction == 1)
                    {
                        memory[memory[index + 3]] = param1 + param2;
                    }
                    else if (instruction == 2)
                    {
                        memory[memory[index + 3]] = param1 * param2;
                    }
                    else if (instruction == 7)
                    {
                        memory[memory[index + 3]] = param1 < param2 ? 1 : 0;
                    }
                    else if (instruction == 8)
                    {
                        memory[memory[index + 3]] = param1 == param2 ? 1 : 0;
                    }
                    index += 4;
                }
                else if (instruction == 3)
                {
                    memory[memory[index + 1]] = input;

                    index += 2;
                }
                else if (instruction == 4)
                {
                    var mode1 = (memory[index] / 100) % 10;

                    int param1;

                    if (mode1 == 0)
                        param1 = memory[memory[index + 1]];
                    else
                        param1 = memory[index + 1];

                    output += $",{param1}";

                    index += 2;
                }
                else if (instruction == 5 || instruction == 6)
                {
                    var mode1 = (memory[index] / 100) % 10;
                    var mode2 = (memory[index] / 1000) % 10;

                    int param1;
                    int param2;

                    if (mode1 == 0)
                        param1 = memory[memory[index + 1]];
                    else
                        param1 = memory[index + 1];

                    if (mode2 == 0)
                        param2 = memory[memory[index + 2]];
                    else
                        param2 = memory[index + 2];

                    if (instruction == 5)
                    {
                        if (param1 != 0)
                            index = param2;
                        else
                            index += 3;
                    }
                    else if (instruction == 6)
                    {

                        if (param1 == 0)
                            index = param2;
                        else
                            index += 3;
                    }
                }
            }

            return output;
        }
